import java.util.Scanner;
class TwoDigit
{
 public static void main(String[] args)
 {
  Scanner sc=new Scanner(System.in);
  System.out.println("Enter the number");
  int x=sc.nextInt();
if(x>=(-99)&&x<=(-10))
{
System.out.println("true");
}
else if(x<=(99)&&x>=(10))
{
System.out.println("true");
}
else{
System.out.println("false");
}
}
}


  
